    public String encriptarClave(){
       String  clave=txtContraseña.getText();
        char array[]=clave.toCharArray();
        for (int i = 0; i < array.length; i++) {
            array[i]=(char)(array[i]+(char)5);  
        }
        String encriptado=String.valueOf(array);
        return encriptado;
    }
